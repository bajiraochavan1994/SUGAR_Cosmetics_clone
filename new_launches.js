  // ------Sort Function-----------
  document.querySelector("#srt").addEventListener("change", mySort)
  
  function mySort(){
    let y=document.querySelector("#srt").value
    if(y=="remove"){
        display(data)
    }else if(y=="asc"){
        let n=data.sort(function(a,b){
            return (a.price-b.price)
        })
        display(n)
    }else if(y=="desc"){
        let o=data.sort(function(a,b){
            return (b.price-a.price)
        })
        display(o)
        }
  }
  
